/**
 * Student 类 (数据模型 / POJO)
 * 
 * 资深工程师视角：
 * 这个问题问得非常深入！这正是从“写代码”转向“面向对象编程 (OOP)”的核心。
 * 简单来说：Student 是一个“模板”或者“设计图”。
 * 
 * 作为资深工程师，我帮你用三个层次来透彻理解：
 * 
 * 1. 形象的比喻：表格的“表头”
 * 想象你在用 Excel 记账：
 * - Student 类：就像 Excel 表格顶部的标题行（学号、姓名、年龄）。
 *   它规定了每一个学生“应该包含哪些信息”，但它本身不是一个具体的人。
 * - Student 对象：就像表格里具体的一行数据（001, 郑寒萍, 18）。
 *   它是根据 Student 这个模板填入真实内容后产生的一个实体。
 * 
 * 2. 代码里的含义：数据打包
 * 在 Java 里，如果你不写 Student 类，你可能需要这样存数据：
 * String id1 = "001"; String name1 = "张三"; int age1 = 18;
 * 这太乱了！如果有 100 个学生，你得写 300 个变量。
 * 有了 Student 类以后，你可以把这三个属性“打包”在一起：
 * Student s = new Student(); // 把学号、姓名、年龄打包成一个整体叫 s
 * 
 * 💡 资深工程师的专业建议：
 * 在软件工程中，我们把 Student 这种类叫做 POJO (Plain Old Java Object)，中文叫“简单的 Java 对象”。
 * 它的唯一任务就是：定义数据长什么样。
 * Student.java 只管数据结构（静态的）。
 */
public class Student {
    // 属性 (数据结构)
    String id;      // 学号
    String name;    // 姓名
    int age;        // 年龄
}
